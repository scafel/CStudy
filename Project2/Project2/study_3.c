void mp(int arr[] , int sz)
{
	for (int i = 0; i < sz -1; i++)
	{
		int flag = 0;
		for (int y = 0; y < sz -1 -i; y++)
		{
			if (arr[y] > arr[y+1])
			{
				int temp = arr[y];
				arr[y] = arr[y + 1];
				arr[y + 1] = temp;
				flag = 1;
			}
		}

		if (flag == 0)
		{
			break;
		}
	}
}


